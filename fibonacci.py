# Write a program to generate the first 10 numbers of the Fibonacci sequence using a while loop.

def fibonacci_series(num):
    fib_sequence=[]
    a,b=0,1
    while(len(fibonacci_series<num)):
        fib_sequence.append(a)
        a,b=b,a+b
        return fibonacci_series
    

num = int(input("Enter the Number: "))
print(fibonacci_series(num))  
